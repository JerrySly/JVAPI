public class AnimeParse {
    private String anime;
    private String nb_votes;
    private String nb_seen;
    private String straight_average;
    private String weighted_average;
    private String bayesian_average;

    public String getAnime() {
        return anime;
    }

    public void setAnime(String anime) {
        this.anime = anime;
    }

    public String getNb_votes() {
        return nb_votes;
    }

    public void setNb_votes(String nb_votes) {
        this.nb_votes = nb_votes;
    }

    public String getNb_seen() {
        return nb_seen;
    }

    public void setNb_seen(String nb_seen) {
        this.nb_seen = nb_seen;
    }

    public String getStraight_average() {
        return straight_average;
    }

    public void setStraight_average(String straight_average) {
        this.straight_average = straight_average;
    }

    public String getWeighted_average() {
        return weighted_average;
    }

    public void setWeighted_average(String weighted_average) {
        this.weighted_average = weighted_average;
    }

    public String getBayesian_average() {
        return bayesian_average;
    }

    public void setBayesian_average(String bayesian_average) {
        this.bayesian_average = bayesian_average;
    }
}
